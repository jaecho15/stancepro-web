"""Generate print-ready StancePro business cards.

Front: brand lockup + name/title/contact + vCard QR (bottom-right)
Back:  brand lockup centered + smart-link QR (stance-pro.com/download)

Output: flat PNG at 300 DPI (3.5" x 2" trim = 1050 x 600 px)
        — add 3mm bleed (+36 px each side) at the printer if required.
"""
