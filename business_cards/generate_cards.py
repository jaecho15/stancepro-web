OUT_DIR.mkdir(parents=True, exist_ok=True)

DPI = 300
CARD_W = int(3.5 * DPI)   # 1050
CARD_H = int(2.0 * DPI)   # 600
MARGIN = 60               # ~5mm safe margin
LOCKUP_SIZE_SCALE = 0.8   # logo + wordmark 20% smaller
LOCKUP_GAP = 8            # horizontal gap hex → wordmark (was 24)
FRONT_WORDMARK_SCALE = 1.2   # front wordmark +20%
BACK_LOCKUP_SCALE = 0.64     # back logo + wordmark vs front (−20% × −20%)

# brand colors
NAVY = (26, 46, 97)
WHITE = (255, 255, 255)
BLUE_LIGHT_ACCENT = (0, 122, 230)     # used on white background
BLUE_DARK_ACCENT = (63, 169, 245)     # used on navy background
MUTED_NAVY = (26, 46, 97, 153)        # ~60% navy for "BY JC KRAFT" on white
MUTED_WHITE = (255, 255, 255, 178)    # ~70% white for "BY JC KRAFT" on navy

# brand assets — see brand_logos.py (generated by build_stancepro_logo_from_crystal.py)

# fonts (macOS system)
FONT_AVENIR = "/System/Library/Fonts/Avenir Next.ttc"
FONT_AVENIR_COND = "/System/Library/Fonts/Avenir Next Condensed.ttc"

# Avenir Next.ttc face indices (verified on this macOS install):
#   0 Bold, 1 BoldItalic, 2 DemiBold, 3 DemiBoldItalic, 4 Italic, 5 Medium,
#   6 MediumItalic, 7 Regular, 8 Heavy, 9 HeavyItalic, 10 UltraLight, 11 UltraLightItalic
AV_REG = 7
AV_MED = 5
AV_DEMI = 2
AV_BOLD = 0
AV_HEAVY = 8


def load_font(path: str, size: int, index: int = 0) -> ImageFont.FreeTypeFont:
    return ImageFont.truetype(path, size=size, index=index)


# ---------------- data ----------------

